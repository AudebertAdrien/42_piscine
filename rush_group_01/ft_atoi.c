void	ft_atoi(int input[16], char *str)
{
	int	i;
	int	j;
	int	num;

	i = 0;
	j = 0;
	num = 0;
	while (str[i])
	{
		while (str[i] >= 9 && str[i] <= 13 || str[i] == 32)
			i++;
		while (str[i] >= 48 && str[i] <= 57)
		{
			input[j] = str[i] - 48;
			j++;
			i++;
		}
		i++;
	}
}
